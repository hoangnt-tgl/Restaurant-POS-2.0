var confirmModifyModal = document.querySelector('.confirm-modify-modal')
var confirmDeleteModal = document.querySelector('.confirm-delete-modal')

// Open Confirm Modify Modal
function openConfirmModifyModal() {
    confirmModifyModal.style.display = 'inline';
}

// Close Confirm Modify Modal
function closeConfirmModifyModal() {
    confirmModifyModal.style.display = 'none';
}

// Open Confirm Delete Modal
function openConfirmDeleteModal() {
    confirmDeleteModal.style.display = 'inline';
}

// Close Confirm Delete Modal
function closeConfirmDeleteModal() {
    confirmDeleteModal.style.display = 'none';
}